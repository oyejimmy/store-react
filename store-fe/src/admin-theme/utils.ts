export const hexToRgbChannel = (hex: string): string => {
  const result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
  return result
    ? `${parseInt(result[1], 16)}, ${parseInt(result[2], 16)}, ${parseInt(result[3], 16)}`
    : '0, 0, 0';
};

export const varAlpha = (rgbChannel: string, alpha: number): string => {
  return `rgba(${rgbChannel}, ${alpha})`;
};